function setup() {
  createCanvas(400, 200);
  angleMode(DEGREES);

}

function draw() {
  colorMode(RGB);
  background('black');
  
  //pac-man
  noStroke();
  fill('yellow');
  arc(100, 100, 160, 160, -130, 130, PIE);

  //ghost
  noStroke();
  fill('Red');
  arc(300, 100, 160, 160, -180, 0, PIE);
  square(220, 100, 160);

  fill('black');
  square(220, 180, 160);

  fill('white');
  circle(260, 90, 50);
  circle(340, 90, 50);

  fill('blue');
  circle(260, 90, 30);
  circle(340, 90, 30);
}
